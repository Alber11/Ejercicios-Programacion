package com.gmail.albermargar9.actividades.actividad13;

public class WhileContador3 {
    public static void main(String[] args) {
        int numero = 1;
        while (numero <= 100){
            System.out.println(numero);
            numero++;
        }
        System.out.println("fin del programa");
        //estamos indicando al programa que imprima el numero hasta 100 en bucle para que
        //la condicion indicada sea true y el bucle se deje de producir
    }
}
