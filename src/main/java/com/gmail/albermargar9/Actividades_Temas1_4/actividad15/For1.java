package com.gmail.albermargar9.Actividades_Temas1_4.actividad15;

public class For1 {
    public static void main(String[] args) {
        System.out.println("********************************************");
        for (int i=10; i>=1; i--){
            System.out.println(i);
        }
        System.out.println("********************************************");
        for (int a=1; a<=10; a++){
            System.out.println(a);
        }
        System.out.println("********************************************");
    }
}
